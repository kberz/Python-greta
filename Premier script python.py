#Saisir deux variables entières dans deux variables a et b et affichez leur sommes leurs différences division multiplication
a = int(input("Entrez une valeur pour a: "))
b = int(input("Entrez une valeur pour b: "))

a_plus_b = a + b
a_moins_b = a - b
a_diviser_b = a / b
a_multiplier_b = a * b

print("La somme des deux est :" + str(a_plus_b))
print("La soustraction des deux est :" + str(a_moins_b))
print("Le produit des deux est :" + str(a_diviser_b))
print("La multipliaction des deux est :" + str(a_multiplier_b))
